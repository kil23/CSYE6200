package edu.neu.csye6200;

public abstract class AbstractPerson {
	
	abstract int getId();
	abstract void setId(int id);
	abstract int getAge();
	abstract String getName();
	abstract int getWeightLbs();
	abstract void setWeightLbs(int weightLbs);
	abstract int getWeightLossLbs();
	abstract void setWeightLossLbs(int weightLossLbs);

}
